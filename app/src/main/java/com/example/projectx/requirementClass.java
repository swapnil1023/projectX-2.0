package com.example.projectx;

public class requirementClass {

    String title;
    String description;

    public requirementClass(String title, String description)
    {
        this.title = title;
        this.description = description;
    }

    public String getTitle()
    {
        return title;
    }

    public String getDescription()
    {
        return description;
    }
}
